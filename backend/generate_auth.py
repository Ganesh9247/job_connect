import com.jobconnect.backend.security.AuthTokenFilter;
import com.jobconnect.backend.security.CustomUserDetailsService;
import com.jobconnect.backend.security.JwtAuthEntryPoint;
import org.springframework.beans.factory.annotation.Autowired;
